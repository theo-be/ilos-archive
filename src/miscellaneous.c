
#include "miscellaneous.h"







/**
 * @fn i_min (int a, int b)
 * @param a premier element
 * @param b deuxieme element
 * @returns le plus petit element
 * @brief Retourne le plus petit element
*/
int i_min (int a, int b) {
    return (a < b ? a : b);
}

/**
 * @fn i_max (int a, int b)
 * @param a premier element
 * @param b deuxieme element
 * @returns le plus grand element
 * @brief Retourne le plus grand element
*/
int i_max (int a, int b) {
    return (a > b ? a : b);
}








